    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
         String employeeName = employeeNameField.getText();
        String employeePosition = employeePositionField.getText();
        String employeeUsername = usernameField.getText();
        String employeePassword = passwordField.getText();

        if (employeeIdField.getText().equals("") || employeeName.equals("") || employeePosition.equals("") || employeeUsername.equals("") || employeePassword.equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Employee ID and other edited details to continue.", "Warning Message", JOptionPane.WARNING_MESSAGE);
        } else {
            int employeeId = Integer.parseInt(employeeIdField.getText());
            if (editOperation(employeeId, employeeName, employeePosition, employeeName, employeePassword)) {
                JOptionPane.showMessageDialog(null, "Employee details Edited Successfully", "Information Message", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Employee Editing Failed", "Error Message", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if (employeeIdDeleteField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Employee ID to delete the Employee.", "Warning Message", JOptionPane.WARNING_MESSAGE);
        } else {
            int employeeId = Integer.parseInt(employeeIdDeleteField.getText());
            if (deleteOperation(employeeId)) {
                JOptionPane.showMessageDialog(null, "Employee Deleted Successfully", "Successful Message", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Employee Deletion Failed", "Error Message", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed


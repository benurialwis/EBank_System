    private static Boolean addOperation(java.lang.String name, java.lang.String position, java.lang.String username, java.lang.String password) {
        employee_webservice.EmployeeService_Service service = new employee_webservice.EmployeeService_Service();
        employee_webservice.EmployeeService port = service.getEmployeeServicePort();
        return port.addOperation(name, position, username, password);
    }

    private static Boolean editOperation(int employeeId, java.lang.String name, java.lang.String position, java.lang.String username, java.lang.String password) {
        employee_webservice.EmployeeService_Service service = new employee_webservice.EmployeeService_Service();
        employee_webservice.EmployeeService port = service.getEmployeeServicePort();
        return port.editOperation(employeeId, name, position, username, password);
    }

    private static Boolean deleteOperation(int employeeId) {
        employee_webservice.EmployeeService_Service service = new employee_webservice.EmployeeService_Service();
        employee_webservice.EmployeeService port = service.getEmployeeServicePort();
        return port.deleteOperation(employeeId);
    }
